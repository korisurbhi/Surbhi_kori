# If statement 
num = 15 
if num % 2 == 0: 
    print(num, "is even") 
else: 
    print(num, "is odd") 
 
# For loop: print fruits 
print("\nFruits in list:") 
for fruit in fruits: 
    print(fruit) 
 
# While loop: 
i = 1 
while i <= 5: 
    print(i) 
    i += 1